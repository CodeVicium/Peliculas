package objlab3;
public class Punto {
    private float y;
    private float x;

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public Punto(float y, float x) {
        this.y = y;
        this.x = x;
    }
 
}
